while_task_5 = '''Сумма чисел. Напишите программу, которая просит пользователя ввести числа. Программа должна продолжать запрашивать числа у пользователя, пока он не введет 'стоп', после чего программа выводит сумму всех введенных чисел.'''

while_task_5_r = '''
sum = 0
user_input = input("Введите число или 'стоп' для завершения: ")

while user_input.lower() != "стоп":
    sum += int(user_input)
    user_input = input("Введите число или 'стоп' для завершения: ")

print("Сумма чисел равна", sum)
'''


