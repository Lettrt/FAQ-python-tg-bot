while_task_2 = 'Угадай число. Напишите игру, где компьютер загадывает случайное число от 1 до 100, а пользователь должен его угадать. Пользователь продолжает угадывать, пока не угадает число, и программа сообщает ему, больше ли или меньше его предыдущего ответа искомое число.'

while_task_2_r = '''
import random

number_to_guess = random.randint(1, 100)
guess = int(input("Угадайте число от 1 до 100: "))

while guess != number_to_guess:
    if guess < number_to_guess:
        print("Больше!")
    else:
        print("Меньше!")
    guess = int(input("Попробуйте снова: "))

print("Поздравляем, вы угадали число!")
'''
while_task_3 =  'Вывод факториала числа. Напишите программу, которая запрашивает у пользователя ввести число и затем вычисляет факториал этого числа, используя цикл while.'
while_task_3_r = '''
number = int(input("Введите число: "))
factorial = 1
i = 1

while i <= number:
    factorial *= i
    i += 1

print("Факториал числа равен", factorial)
'''

